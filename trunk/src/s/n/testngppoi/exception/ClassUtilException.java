package s.n.testngppoi.exception;

public class ClassUtilException extends Exception {

	private static final long serialVersionUID = 9163855117027349973L;

	public ClassUtilException() {
		super();
	}

	public ClassUtilException(String message) {
		super(message);
	}

	public ClassUtilException(String message, Throwable cause) {
		super(message, cause);
	}

	public ClassUtilException(Throwable cause) {
		super(cause);
	}
}
